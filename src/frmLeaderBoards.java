    private void btnFindScoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindScoreActionPerformed
        
        ArrayList <String> scores = new ArrayList <String>(); //creates an ArrayList of scores
        BufferedReader br = null;
        
        try{ //buffered reader adds books to the ArrayList
            br = new BufferedReader(new FileReader("scores.txt"));
            String score;
            while ((score = br.readLine()) != null ){
                scores.add(score);
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                br.close();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
        
        String [] highscores = new String[scores.size()];//creates a String array from the bookList array list
        scores.toArray(highscores);        
        
        //declare variable to store index of found name
        int foundIndex = -1;
        //declare variable to store user input
        String name = txtName.getText();
        
        //searches for index of the value of name
        for (int i = 0; i<highscores.length; i++){
            if (highscores[i].equals(name)){
                foundIndex=i;
            }
        }
        
        if (foundIndex==-1){//if name is not fount
            lblFoundScore.setText("Not Found");
        }
        else{//when name is found
            lblFoundScore.setText(highscores[foundIndex+1]);//displays score for user
        }
        
    }//GEN-LAST:event_btnFindScoreActionPerformed

