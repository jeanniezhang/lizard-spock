    public static int[] findTop3(ArrayList<String> a){
        //declares variables
        int first=0, second=0, third=0;//variables to store indices
        int temp, compTemp = 1000;//temporary variables used for comparison
        
        for (int i = 1; i<a.size(); i=i+2){//traverses the array list for scores
            temp = Integer.parseInt(a.get(i));
            if (temp<compTemp){
                first = i;//set index of first high score
                compTemp = temp;
            }
        }
        
        compTemp = 1000;//resets temporary variable
        for (int i = 1; i<a.size(); i=i+2){//traverses the array list for scores
            temp = Integer.parseInt(a.get(i));
            if (temp<compTemp&i!=first){
                second = i;//set index of second high score
                compTemp = temp;
            }
        }
        
        compTemp = 1000;//resets temporary variable
        for (int i = 1; i<a.size(); i=i+2){//traverses the array list for scores
            temp = Integer.parseInt(a.get(i));
            if (temp<compTemp&i!=first&i!=second){
                third = i;//set index of third high score
                compTemp = temp;
            }
        }
        
        
        int[] indices = {first, second, third};
        return indices;//returns an integer array of indices
    }
    
